public class Adresse {
//Attributs
int numrue,nomrue,codepostal,codeville;
//Constructors
public Adresse(int numrue, int nomrue, int codepostal, int codeville) {
	this.numrue = numrue;
	this.nomrue = nomrue;
	this.codepostal = codepostal;
	this.codeville = codeville;
}
public Adresse() {}
//getters & setters 
public int getNumrue() {
	return numrue;
}
public void setNumrue(int numrue) {
	this.numrue = numrue;
}
public int getNomrue() {
	return nomrue;
}
public void setNomrue(int nomrue) {
	this.nomrue = nomrue;
}
public int getCodepostal() {
	return codepostal;
}
public void setCodepostal(int codepostal) {
	this.codepostal = codepostal;
}
public int getCodeville() {
	return codeville;
}
public void setCodeville(int codeville) {
	this.codeville = codeville;
}
//Methods
}
